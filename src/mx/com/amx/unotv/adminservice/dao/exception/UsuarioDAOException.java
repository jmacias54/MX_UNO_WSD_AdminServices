package mx.com.amx.unotv.adminservice.dao.exception;

public class UsuarioDAOException extends Exception{

	private static final long serialVersionUID = 1L;

	public UsuarioDAOException() {
		super();
	}

	public UsuarioDAOException(String message, Throwable cause) {
		super(message, cause);
	}

	public UsuarioDAOException(String message) {
		super(message);
	}

	public UsuarioDAOException(Throwable cause) {
		super(cause);
	}
	

}
