package mx.com.amx.unotv.adminservice.dao.exception;

public class NNotaDAOException extends Exception{

	private static final long serialVersionUID = 1L;

	public NNotaDAOException() {
		super();
	}

	public NNotaDAOException(String message, Throwable cause) {
		super(message, cause);
	}

	public NNotaDAOException(String message) {
		super(message);
	}

	public NNotaDAOException(Throwable cause) {
		super(cause);
	}
	

}
