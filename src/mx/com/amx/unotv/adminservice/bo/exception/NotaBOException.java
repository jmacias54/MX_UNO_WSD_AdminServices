package mx.com.amx.unotv.adminservice.bo.exception;

public class NotaBOException extends Exception{

	private static final long serialVersionUID = 1L;

	public NotaBOException() {
		super();
	}

	public NotaBOException(String message, Throwable cause) {
		super(message, cause);
	}

	public NotaBOException(String message) {
		super(message);
	}

	public NotaBOException(Throwable cause) {
		super(cause);
	}
	



}
